CREATE DATABASE db_generation_game_online;

USE db_generation_game_online;

CREATE TABLE tb_classes(ID BIGINT(5) AUTO_INCREMENT,
CLASSES VARCHAR(255) NOT NULL,
PRIMARY KEY(ID));

CREATE TABLE tb_personagens(ID BIGINT(8) AUTO_INCREMENT,
PERSONAGEM VARCHAR(255),
ATAQUE INT,
DEFESA INT,
ORIGEM VARCHAR(255),
CATEGORIA_ID BIGINT,
PRIMARY KEY(ID),
FOREIGN KEY(CATEGORIA_ID) REFERENCES tb_classes(ID));

